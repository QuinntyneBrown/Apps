    /// </summary>
    Appliances = 2,

    /// <summary>
    /// Jewelry and valuables.
    /// </summary>
    Jewelry = 3,

    /// <summary>
    /// Collectibles and antiques.
    /// </summary>
    Collectibles = 4,

