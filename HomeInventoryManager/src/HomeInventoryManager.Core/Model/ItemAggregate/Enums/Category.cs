    /// Clothing and accessories.
    /// </summary>
    Clothing = 6,

    /// <summary>
    /// Books and media.
    /// </summary>
    Books = 7,

    /// <summary>
    /// Sports and recreation equipment.
    /// </summary>
    Sports = 8,

    /// <summary>
    /// Other items.
    /// </summary>
    Other = 9,
}
