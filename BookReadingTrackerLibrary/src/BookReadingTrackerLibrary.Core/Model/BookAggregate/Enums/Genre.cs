    /// </summary>
    Fiction = 0,

    /// <summary>
    /// Non-fiction genre.
    /// </summary>
    NonFiction = 1,

    /// <summary>
    /// Mystery genre.
    /// </summary>
    Mystery = 2,

    /// <summary>
    /// Science fiction genre.
    /// </summary>
    ScienceFiction = 3,

    /// <summary>
    /// Fantasy genre.
    /// </summary>
    Fantasy = 4,

    /// <summary>
    /// Biography genre.
    /// </summary>
    Biography = 5,

    /// <summary>
    /// History genre.
    /// </summary>
    History = 6,

    /// <summary>
    /// Romance genre.
    /// </summary>
    Romance = 7,

    /// <summary>
    /// Thriller genre.
    /// </summary>
    Thriller = 8,

    /// <summary>
    /// Self-help genre.
    /// </summary>
    SelfHelp = 9,

    /// <summary>
    /// Other genre.
    /// </summary>
    Other = 10,
}
